/**
 * Escape_sequence
 */
public class Escape_sequence {

    public static void main(String[] args) {
        System.out.println("Al Amin Hossian Rifat");
        System.out.println("\"Al Amin Hossian Rifat\"");
        System.out.println("Al Amin Hossain Rifat");
        System.out.println("01518326121");
        // All Code In One line
        System.out.println("\"Al Amin Hossain Rifat\" \n 01518326121 \n Demra,Dhaka,Bangladesh");


        System.out.println("Name \t GPA");
        System.out.println("Rifat \t 5.00");
        System.out.println("XYZ \t 3.00");
    }
}